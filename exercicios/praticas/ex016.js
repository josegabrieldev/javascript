// Contagem com passo fixo

console.log('Vou conta de 0 até 50 pulando de 5 em 5')
for (var c = 0; c <= 50; c = c + 5) {
    console.log(`${c}`)
}
console.log('FIM DA CONTAGEM!')

console.log('vou conta de 100 até 0 pulando de 10 em 10')
for (var n = 100; n >= 0; n = n - 10) {
    console.log(`${n}`)
}
console.log('FIM DA CONTAGEM!')
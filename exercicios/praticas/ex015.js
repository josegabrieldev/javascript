// Contagem simples usando while

//Contagem crescente ate 10
var n = 1
console.log('Iniciando Contagem!')
while (n <= 10) {
    console.log(`Contagem no ${n}`)
    n++
}
console.log('FIM da contagem!')

// Contagem regressiva de 10 ate 1
var r = 10
console.log('Contagem regressiva iniciada')
while (r >= 1) {
    console.log(`Contagem em ${r}`)
    r = r - 1
}
console.log('Contagem regressiva encerrada')